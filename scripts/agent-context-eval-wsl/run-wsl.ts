/**
 * agent-context validation experiment — WSL Android replication.
 *
 * Supplements the DeptAgent PoC (docs/reference/agent-context-validation.md)
 * by repeating the experiment on a real legacy capability (video-playback)
 * with the three methodology changes the PoC prescribed for WSL:
 *
 *   1. Raw-code is NOT pre-curated to a tiny set. We measure two raw-code
 *      conditions: B_curated (the minimal correct file set the
 *      agent-context's read-order points to — the best case for raw code)
 *      and B_full (every file in the capability — the "no map, read
 *      everything" case that proxies real search overhead).
 *   2. The judge is a DIFFERENT model from the answerer (answerer:
 *      claude-sonnet-4-6, judge: claude-opus-4-8) to remove the
 *      same-model cross-eval bias.
 *   3. A discovery-category question is included (Q11) that tests the
 *      search-elimination value directly.
 *
 * This script lives in the DeptAgent repo (which has the tsx + SDK
 * tooling) but reads all source + agent-context from the WSL Android repo
 * via absolute paths. It writes NOTHING into the Android repo.
 *
 * Run from the deptagent-poc repo root:
 *   pnpm tsx scripts/agent-context-eval-wsl/run-wsl.ts
 *
 * Reads ANTHROPIC_API_KEY from .env.local.
 */

import Anthropic from "@anthropic-ai/sdk";
import fs from "fs";
import dotenv from "dotenv";

dotenv.config({ path: ".env.local" });

if (!process.env.ANTHROPIC_API_KEY) {
  console.error("ANTHROPIC_API_KEY not set (looked in .env.local). Exiting.");
  process.exit(1);
}

const client = new Anthropic();
const ANSWER_MODEL = "claude-sonnet-4-6";
const JUDGE_MODEL = "claude-opus-4-8";
const MAIN_MAX_TOKENS = 600;
const JUDGE_MAX_TOKENS = 200;

const WSL_ROOT = "/Users/tribalscale/tk-work/wsl-android/wsl-android";
const QUESTIONS_PATH = "scripts/agent-context-eval-wsl/questions.json";
// The agent-context.md under test is vendored into this repo so the
// experiment is self-contained. It was generated by /docs-bootstrap
// against the WSL repo; only the bulky raw source is still read from
// WSL_ROOT at replication time (read-only — nothing is written there).
const AGENT_CONTEXT_PATH = "scripts/agent-context-eval-wsl/video-playback-agent-context.md";

const SRC = `${WSL_ROOT}/app/src/main/java/com/wsl`;

// B_curated: the minimal set the agent-context read-order points to. This
// is the "agent magically knows which files matter" lower bound for raw
